package tk.dto.group;

public class DisplayBelongsDTO {
	
	private int belongsno;
	private String belongsnm;
	private String castle;
	private String city;
	
	public int getBelongsno() {
		return belongsno;
	}
	public void setBelongsno(int belongsno) {
		this.belongsno = belongsno;
	}
	public String getBelongsnm() {
		return belongsnm;
	}
	public void setBelongsnm(String belongsnm) {
		this.belongsnm = belongsnm;
	}
	public String getCastle() {
		return castle;
	}
	public void setCastle(String castle) {
		this.castle = castle;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
}